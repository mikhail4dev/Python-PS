# **Описание**: Напишите программу, которая перебирает список оценок студентов с помощью enumerate и выводит номер по порядку и оценку для каждого студента, пропуская оценки ниже 60 баллов.
#
# **Входные данные**: Список целых чисел grades, встроенный в код:
# grades = [85, 42, 91, 55, 73, 38, 66]
#
# **Выходные данные**: Строки вида "Студент N: X баллов",
# где N — порядковый номер начиная с 1, X — оценка.
# Оценки строго ниже 60 пропускаются.
#
# **Ограничения**: Использовать enumerate и continue для пропуска оценок ниже 60.
# Нумерация студентов начинается с 1.
#
# **Примеры**:
# Input: grades = [85, 42, 91, 55, 73, 38, 66]
# Output:
# Студент 1: 85 баллов
# Студент 3: 91 баллов
# Студент 5: 73 баллов
# Студент 7: 66 баллов

grades = [85, 42, 91, 55, 73, 38, 66]

# Ваш код здесь

for i, score in enumerate(grades):
    if score < 60:
        continue
    print(f"Студент {i + 1}: {score} баллов")