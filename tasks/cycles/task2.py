# **Описание**: Напишите программу, которая находит первое число в списке, кратное заданному делителю, и прекращает поиск сразу после его нахождения.
#
# **Входные данные**: Список целых чисел `numbers` и делитель `divisor`, встроенные в код:
# numbers = [3, 7, 11, 14, 19, 28, 35]
# divisor = 7
#
# **Выходные данные**: Одна строка с первым найденным числом, кратным делителю. Если таких чисел нет — вывести "Не найдено".
#
# **Ограничения**: Использовать цикл `for` и оператор `break` для остановки после первого найденного числа. Не использовать встроенные функции поиска.
#
# **Примеры**:
# Input: numbers = [3, 7, 11, 14, 19, 28, 35], divisor = 7
# Output: 7
#
# Input: numbers = [1, 2, 4, 8, 16], divisor = 7
# Output: Не найдено

numbers = [3, 7, 11, 14, 19, 28, 35]
divisor = 7

# Ваш код здесь

for number in numbers:
    if number % divisor == 0:
        print(number)
        break
else:
    print("Не найдено")