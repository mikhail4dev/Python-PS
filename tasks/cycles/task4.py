# **Описание**: Напишите программу, которая использует цикл `while` для вывода таблицы умножения заданного числа от 1 до 10.
#
# **Входные данные**: Целое число `multiplier`, встроенное в код:
# multiplier = 6
#
# **Выходные данные**: 10 строк вида "6 x N = результат", где N — множитель от 1 до 10.
#
# **Ограничения**: Использовать исключительно цикл `while` со счётчиком.
# Счётчик начинается с 1 и заканчивается на 10 включительно.
#
# **Примеры**:
# Input: multiplier = 6
# Output:
# 6 x 1 = 6
# 6 x 2 = 12
# ...
# 6 x 10 = 60

multiplier = 6

# Ваш код здесь

counter = 1

while counter <= 10:
    print(f"{multiplier} x {counter} = {multiplier * counter}")
    counter += 1