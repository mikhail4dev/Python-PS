# **Описание**: Напишите программу, которая перебирает список цен на товары и вычисляет общую сумму, используя цикл `for`. После завершения цикла блок `else` должен вывести итоговую сумму.
#
# **Входные данные**: Список целых чисел `prices`, встроенный в код:
# prices = [120, 450, 89, 310, 75, 200]
#
# **Выходные данные**: Одна строка вида "Итого: X рублей", где X — сумма всех элементов списка. Строка выводится из блока `else`.
#
# **Ограничения**: Использовать цикл `for` и блок `else` после него. Накапливать сумму в переменной-счётчике внутри цикла. Не использовать встроенную функцию `sum()`.
#
# **Примеры**:
# Input: prices = [120, 450, 89, 310, 75, 200]
# Output: Итого: 1244 рублей
#
# Input: prices = [500, 300, 200]
# Output: Итого: 1000 рублей

prices = [120, 450, 89, 310, 75, 200]
total = 0

# Ваш код здесь

for i in prices:
    total += i
else: 
    print(f"Итого: {total} рублей")