# **Описание**: Напишите программу, которая перебирает список покупок и выводит каждый товар на отдельной строке.
#
# **Входные данные**: Список строк `shopping_list`, встроенный в код:
# shopping_list = ["яблоки", "молоко", "хлеб", "сыр", "масло"]
#
# **Выходные данные**: Каждый элемент списка напечатан на отдельной строке в том же порядке, в котором он находится в списке.
#
# **Ограничения**: Использовать цикл `for` для перебора элементов. Не изменять исходный список.
#
# **Примеры**:
# Input: shopping_list = ["яблоки", "молоко", "хлеб", "сыр", "масло"]
# Output:
# яблоки
# молоко
# хлеб
# сыр
# масло

shopping_list = ["яблоки", "молоко", "хлеб", "сыр", "масло"]

# Ваш код здесь

for i in shopping_list:
    print(f"{i}")