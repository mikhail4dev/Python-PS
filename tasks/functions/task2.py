#
# **Входные данные**: Произвольное количество позиционных аргументов — числа с плавающей точкой (цены товаров).
#
# **Выходные данные**: Сумма всех переданных цен (float), либо `None`, если аргументы не переданы.
#
# **Ограничения**:
# - Используйте `*args` для приёма произвольного числа аргументов
# - При отсутствии аргументов возвращайте именно `None`, не `0`
# - Все цены — неотрицательные числа
#
# **Примеры**:
# Input: total_cost(10.0, 20.0, 5.5)
# Output: 35.5
#
# Input: total_cost(99.9)
# Output: 99.9
#
# Input: total_cost()
# Output: None

