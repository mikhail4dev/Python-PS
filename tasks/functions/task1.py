# **Описание**: Реализуйте функцию `make_label`, которая принимает название товара и его цену,
# и возвращает строку-ярлык. Если валюта не указана, по умолчанию используется "RUB".
#
# **Входные данные**:
#   - name (str) — название товара
#   - price (float) — цена товара
#   - currency (str) — обозначение валюты, по умолчанию "RUB"
#
# **Выходные данные**: строка формата "[название]: [цена] [валюта]"
#
# **Ограничения**:
#   - Цена передаётся как число с плавающей точкой
#   - Все три вызова ниже должны выводить корректный результат
#
# **Примеры**:
#   make_label("Молоко", 89.9)           -> "Молоко: 89.9 RUB"
#   make_label("Coffee", 3.5, "USD")     -> "Coffee: 3.5 USD"
#   make_label("Чай", 120.0, "RUB")     -> "Чай: 120.0 RUB"

def make_label(name:str, price:float, currency:str="RUB") -> str:
    # Ваш код здесь
    return f"{name}: {str(price)} {currency}"


# Проверочные вызовы (не изменяйте эти строки)
print(make_label("Молоко", 89.9))
print(make_label("Coffee", 3.5, "USD"))
print(make_label("Чай", 120.0, "RUB"))