# **Описание**: Напишите функцию `describe_item`, которая принимает название предмета, его количество и необязательный единицу измерения, и возвращает строку-описание. Вызовите функцию три раза: дважды с единицей измерения и один раз без неё.
#
# **Входные данные**:
# - `name` (str) — название предмета
# - `quantity` (int) — количество
# - `unit` (str) — единица измерения, по умолчанию "шт"
#
# **Выходные данные**: строка формата "[название]: [количество] [единица]"
#
# **Ограничения**:
# - Функция должна возвращать строку, а не печатать её
# - Все три вызова функции должны быть выведены через print
# - Не используйте импорты
#
# **Примеры**:
# Input: describe_item("Карандаш", 5)
# Output: Карандаш: 5 шт
#
# Input: describe_item("Молоко", 2, "л")
# Output: Молоко: 2 л
#
# Input: describe_item("Ткань", 10, "м")
# Output: Ткань: 10 м

def describe_item(name:str, quantity:int, unit:str = "шт") -> str:
    # Ваш код здесь
    return f"{name}: {str(quantity)} {unit}"


print(describe_item("Карандаш", 5))
print(describe_item("Молоко", 2, "л"))
print(describe_item("Ткань", 10, "м"))