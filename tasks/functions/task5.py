# **Описание**: Напишите функцию `training_summary`, которая принимает название упражнения,
# количество подходов и количество повторений в каждом подходе, и возвращает строку-сводку тренировки.
# Вызовите функцию три раза, передавая аргументы по имени.
#
# **Входные данные**:
# - `exercise` (str) — название упражнения
# - `sets` (int) — количество подходов
# - `reps` (int) — количество повторений в подходе
#
# **Выходные данные**: строка формата "[название]: [подходы] подх. x [повторения] повт."
#
# **Ограничения**:
# - Функция должна возвращать строку, а не печатать её
# - Все три вызова должны передавать аргументы по имени (keyword arguments)
# - Все три вызова должны быть выведены через print
# - Не используйте импорты
#
# **Примеры**:
# Input: training_summary(exercise="Отжимания", sets=3, reps=15)
# Output: Отжимания: 3 подх. x 15 повт.
#
# Input: training_summary(exercise="Приседания", sets=4, reps=12)
# Output: Приседания: 4 подх. x 12 повт.
#
# Input: training_summary(exercise="Планка", sets=2, reps=30)
# Output: Планка: 2 подх. x 30 повт.

def training_summary(exercise:str, sets:int, reps:int) -> str:
    # Ваш код здесь
    return f"{exercise}: {sets} подх. x {reps} повт."


# Вызовите функцию три раза, передавая аргументы по имени
print(training_summary(exercise="Отжимания", sets=3, reps=15))
print(training_summary(exercise="Приседания", sets=4, reps=12))
print(training_summary(exercise="Планка", sets=2, reps=30))