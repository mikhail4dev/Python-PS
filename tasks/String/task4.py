# **Описание**: Создайте программу для преобразования строки в разные регистры с помощью встроенных методов Python
# 
# **Входные данные**: Переменная text задана в коде со значением строки
# 
# **Выходные данные**: Четыре отдельные строки с результатами преобразований:
# - "Верхний регистр: [результат]"
# - "Нижний регистр: [результат]"
# - "Заглавные буквы: [результат]"
# - "Инвертированный регистр: [результат]"
# 
# **Ограничения**: Использовать только методы upper(), lower(), title(), swapcase() для преобразования строки
# 
# **Примеры**:
# Входные данные: text = "hello WORLD"
# Выходные данные:
# Верхний регистр: HELLO WORLD
# Нижний регистр: hello world
# Заглавные буквы: Hello World
# Инвертированный регистр: HELLO world
# 
# Входные данные: text = "PyThOn ProGrAmMiNg"
# Выходные данные:
# Верхний регистр: PYTHON PROGRAMMING
# Нижний регистр: python programming
# Заглавные буквы: Python Programming
# Инвертированный регистр: pYtHoN pROgRAmmInG

# Входная строка для преобразования
text = "hello WORLD"

# Ваш код здесь

print(text.upper())
print(text.lower())
print(text.title())
print(text.swapcase())