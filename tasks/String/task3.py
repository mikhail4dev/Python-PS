# **Описание**: Создайте программу для проверки свойств строки с помощью встроенных методов Python
#
# **Входные данные**: Переменная text задана в коде со значением строки
#
# **Выходные данные**: Четыре отдельные строки с результатами проверок в следующем формате:
# - "Состоит только из цифр: [True/False]"
# - "Начинается с 'test': [True/False]"
# - "Заканчивается на '.com': [True/False]"
# - "Содержит '@': [True/False]"
#
# **Ограничения**: Использовать только методы isdigit(), startswith(), endswith() и оператор in для проверки содержания символов
#
# **Примеры**:
# Входные данные: text = "test@example.com"
# Выходные данные:
# Состоит только из цифр: False
# Начинается с 'test': True
# Заканчивается на '.com': True
# Содержит '@': True
#
# Входные данные: text = "12345"
# Выходные данные:
# Состоит только из цифр: True
# Начинается с 'test': False
# Заканчивается на '.com': False
# Содержит '@': False

text = "test@example.com"

# Ваш код здесь

if text.isdigit():
    print("Состоит только из цифр: True")
else:
    print("Состоит только из цифр: False")

if text.startswith("test"):
    print("Начинается с 'test': True")
else:
    print("Начинается с 'test': False")

if text.endswith(".com"):
    print("Заканчивается на '.com': True")
else:
    print("Заканчивается на '.com': False")

if "@" in text:
    print("Содержит '@': True")
else:
    print("Содержит '@': False")